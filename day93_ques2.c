#include <stdio.h>
#include <string.h>

// Day 93 - Advanced - KMP Algorithm
// Additional practice

int main() {
    printf("Day 93 - Practice: Advanced - KMP Algorithm\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}