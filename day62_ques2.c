#include <stdio.h>
#include <string.h>

// Day 62 - Graph - Cycle Detection
// Additional practice

int main() {
    printf("Day 62 - Practice: Graph - Cycle Detection\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}