#include <stdio.h>
#include <string.h>

// Day 100 - Advanced - Red Black Tree
// Additional practice

int main() {
    printf("Day 100 - Practice: Advanced - Red Black Tree\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}