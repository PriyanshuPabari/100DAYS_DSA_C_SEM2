#include <stdio.h>
#include <string.h>

// Day 88 - Trie - Insert and Search
// Additional practice

int main() {
    printf("Day 88 - Practice: Trie - Insert and Search\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}