#include <stdio.h>
#include <string.h>

// Day 63 - Hashing - Hash Table
// Additional practice

int main() {
    printf("Day 63 - Practice: Hashing - Hash Table\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}