#include <stdio.h>
#include <string.h>

// Day 99 - Advanced - AVL Tree
// Additional practice

int main() {
    printf("Day 99 - Practice: Advanced - AVL Tree\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}