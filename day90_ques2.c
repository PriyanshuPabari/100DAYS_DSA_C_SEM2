#include <stdio.h>
#include <string.h>

// Day 90 - Segment Tree - Range Query
// Additional practice

int main() {
    printf("Day 90 - Practice: Segment Tree - Range Query\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}