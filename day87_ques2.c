#include <stdio.h>
#include <string.h>

// Day 87 - Trie - Implementation
// Additional practice

int main() {
    printf("Day 87 - Practice: Trie - Implementation\n");
    
    int n;
    printf("Enter n: ");
    scanf("%d", &n);
    
    for (int i = 1; i <= n; i++) {
        printf("%d ", i * i);
    }
    printf("\n");
    
    return 0;
}